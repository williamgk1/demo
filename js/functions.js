function getVal (stringAmount)
{
	if (stringAmount == undefined) return 0;

	var amount = 0;
	var isNegative = false;

	stringAmount = (stringAmount == '')? '0' : stringAmount;
	stringAmount = stringAmount.toString();
	//console.log ('stringAmount::'+stringAmount);
	if (stringAmount !== '0')
	{
		if (stringAmount.indexOf ('(') != -1)
		{
			isNegative = true;
			stringAmount = stringAmount.replace ('(', '');
			stringAmount = stringAmount.replace (')', '');
		}
		//if (stringAmount.indexOf (',') != -1) stringAmount = removeThousandSep (stringAmount);
	}

	amount = parseFloat (stringAmount).toFixed (2);
	//console.log ('amount::'+amount);
	if (isNegative) amount = amount * -1;

	return amount;
}



function populateTable (tableId)
{
	var dataKeys = Object.keys(appData);
	var trnData = appData['transactions'];
	//console.log (trnData);
	var trnTbody = '';
	if (Object.keys(trnData) != '')
	{
		trnTbody += '<thead>'+
									'<tr class="text-white">'+
									'	<th>Date</th>'+
									'	<th>Account</th>'+
									'	<th>Fund</th>'+
									'	<th>Transaction Type</th>'+
									'	<th>Transaction Description</th>'+
									'	<th>Price Per Unit</th>'+
									'	<th>Units</th>'+
									'	<th>Currency</th>'+
									'	<th>Net Amount</th>'+
									'	<th>&nbsp;</th>'+
									'</tr>'+
								'</thead>'+
								'<tbody id="trnBody">';
		for (index in trnData)
		{
			trnTbody += '<tr>';
			//console.log ('index ['+index+']::'+trnData[index]);
			for (trnField in trnData[index])
			{
				//console.log ('trnField::'+trnField+' = '+trnData[index][trnField]);
				if (trnField === 'NetAmount')
				{
					trnTbody += '<td>'+trnData[index][trnField].toFixed (2)+'</td>';
				}
				else
				{
					trnTbody += '<td>'+trnData[index][trnField]+'</td>';
				}
			}
			trnTbody += '<td><span href="#" class="btn p-0 text-2" data-bs-toggle="modal" data-bs-target="#trnQuery" alt="Want to query this transaction?" title="Want to query this transaction?" id="trn_'+index+'" onclick="populateTrnModal('+index+');"></span></td>';
			trnTbody += '</tr>';
		}
		trnTbody += '</tbody>';

		if (trnTbody != '')
		{
			$("#"+tableId).html(trnTbody);
			$('#'+tableId).dataTable(
			{
				responsive: true,
			});
		}

	}
}



